var variable1= parseInt(prompt("Ingrese el valor de la variable 1"));
var variable2= parseInt(prompt("Ingrese el valor de la variable 2")); 


function sumar(variable1, variable2){
    return(variable1+variable2)
}

function restar(variable1, variable2){
    return(variable1-variable2)
}

function multiplicar(variable1, variable2){
    return(variable1*variable2)
}

function dividir(variable1, variable2){
    return(variable1/variable2)
}